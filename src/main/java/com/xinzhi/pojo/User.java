package com.xinzhi.pojo;

public class User {
	private Integer uid;
	private String uname;
	private Integer uage;
	private String usex;
	private String uphone;
	private Integer uaddress;
	private String umail;
	private Integer ucodetype;
	private String ucodeenumber;
	public Integer getUid() {
		return uid;
	}
	public void setUid(Integer uid) {
		this.uid = uid;
	}
	public String getUname() {
		return uname;
	}
	public void setUname(String uname) {
		this.uname = uname;
	}
	public Integer getUage() {
		return uage;
	}
	public void setUage(Integer uage) {
		this.uage = uage;
	}
	public String getUsex() {
		return usex;
	}
	public void setUsex(String usex) {
		this.usex = usex;
	}
	public String getUphone() {
		return uphone;
	}
	public void setUphone(String uphone) {
		this.uphone = uphone;
	}
	public Integer getUaddress() {
		return uaddress;
	}
	public void setUaddress(Integer uaddress) {
		this.uaddress = uaddress;
	}
	public String getUmail() {
		return umail;
	}
	public void setUmail(String umail) {
		this.umail = umail;
	}
	public Integer getUcodetype() {
		return ucodetype;
	}
	public void setUcodetype(Integer ucodetype) {
		this.ucodetype = ucodetype;
	}
	public String getUcodeenumber() {
		return ucodeenumber;
	}
	public void setUcodeenumber(String ucodeenumber) {
		this.ucodeenumber = ucodeenumber;
	}
	
}
