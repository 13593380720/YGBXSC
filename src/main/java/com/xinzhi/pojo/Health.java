package com.xinzhi.pojo;

public class Health {
	private Integer uid;
	private String utype;
	private String uspecific;
	public Integer getUid() {
		return uid;
	}
	public void setUid(Integer uid) {
		this.uid = uid;
	}
	public String getUtype() {
		return utype;
	}
	public void setUtype(String utype) {
		this.utype = utype;
	}
	public String getUspecific() {
		return uspecific;
	}
	public void setUspecific(String uspecific) {
		this.uspecific = uspecific;
	}
}
