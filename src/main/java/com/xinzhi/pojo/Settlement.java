package com.xinzhi.pojo;

public class Settlement {
	private Integer uid;
	private Integer umoney;
	private String umaterials;
	private Integer utypeid;
	private Integer upayorder;
	private Integer ubeneficiary;
	public Integer getUid() {
		return uid;
	}
	public void setUid(Integer uid) {
		this.uid = uid;
	}
	public Integer getUmoney() {
		return umoney;
	}
	public void setUmoney(Integer umoney) {
		this.umoney = umoney;
	}
	public String getUmaterials() {
		return umaterials;
	}
	public void setUmaterials(String umaterials) {
		this.umaterials = umaterials;
	}
	public Integer getUtypeid() {
		return utypeid;
	}
	public void setUtypeid(Integer utypeid) {
		this.utypeid = utypeid;
	}
	public Integer getUpayorder() {
		return upayorder;
	}
	public void setUpayorder(Integer upayorder) {
		this.upayorder = upayorder;
	}
	public Integer getUbeneficiary() {
		return ubeneficiary;
	}
	public void setUbeneficiary(Integer ubeneficiary) {
		this.ubeneficiary = ubeneficiary;
	}
}
